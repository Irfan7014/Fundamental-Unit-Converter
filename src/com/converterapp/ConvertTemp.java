    private void convertbuttonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_convertbuttonMouseClicked
        // TODO add your handling code here:
        String from,to,ans,convert;
        from=fromunit.getSelectedItem().toString();
        to=tounit.getSelectedItem().toString();
        convert=temptext.getText();
        if (from.equals("Kelvin") && to.equals("Kelvin"))   //1
        {
            ans=kelvintokelvin(convert);
            convertedtemp.setText(convert+" K = "+ans+" K");
        }
        else if(from.equals("Kelvin") && to.equals("Celsius"))   //2
        {
            ans=kelvintocelsius(convert);
            convertedtemp.setText(convert+" K = "+ans+" °C");
        }
        else if(from.equals("Kelvin") && to.equals("Farenheit"))   //3
        {
            ans=kelvintofarenheit(convert);
            convertedtemp.setText(convert+" K = "+ans+" °F");
        }
        else if(from.equals("Celsius") && to.equals("Kelvin"))   //4
        {
            ans=celsiustokelvin(convert);
            convertedtemp.setText(convert+" °C = "+ans+" K");
        }
        else if(from.equals("Celsius") && to.equals("Celsius"))   //5
        {
            ans=celsiustocelsius(convert);
            convertedtemp.setText(convert+" °C = "+ans+" °C");
        }
        else if(from.equals("Celsius") && to.equals("Farenheit"))   //6
        {
            ans=celsiustofarenheit(convert);
            convertedtemp.setText(convert+" °C = "+ans+" °F");
        }
        else if(from.equals("Farenheit") && to.equals("Kelvin"))   //7
        {
            ans=farenheittokelvin(convert);
            convertedtemp.setText(convert+" °F = "+ans+" K");
        }
        else if(from.equals("Farenheit") && to.equals("Celsius"))   //8
        {
            ans=farenheittocelsius(convert);
            convertedtemp.setText(convert+" °F = "+ans+" °C");
        }
        else if(from.equals("Farenheit") && to.equals("Farenheit"))   //9
        {
            ans=farenheittofarenheit(convert);
            convertedtemp.setText(convert+" °F = "+ans+" °F");
        }
    }//GEN-LAST:event_convertbuttonMouseClicked

