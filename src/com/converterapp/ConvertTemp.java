    public String kelvintokelvin(String val)  //1) K to K
    {
        return val;
    }
    public String celsiustocelsius(String val)  //2) °C to °C
    {
        return val;
    }
    public String farenheittofarenheit(String val)  //3) °F to °F
    {
        return val;
    }
    public String kelvintocelsius(String val)  //4) K to °C
    {
        String ans=val;
        double input=Double.parseDouble(val);
        ans=Double.toString(input-273.15);
        return ans;
    }
    public String kelvintofarenheit(String val)  //5) K to °F
    {
        String ans=val;
        double input=Double.parseDouble(val);
        ans=Double.toString(((input-273.15)*1.8)+32);
        return ans;
    }
    public String celsiustokelvin(String val)  //6) °C to K
    {
        String ans=val;
        double input=Double.parseDouble(val);
        ans=Double.toString(input+273.15);
        return ans;
    }
    public String celsiustofarenheit(String val)  //7) °C to °F
    {
        String ans=val;
        double input=Double.parseDouble(val);
        ans=Double.toString((input*1.8)+32);
        return ans;
    }
    public String farenheittocelsius(String val)  //8) °F to °C
    {
        String ans=val;
        double input=Double.parseDouble(val);
        ans=Double.toString((input-32)*5/9);
        return ans;
    }
    public String farenheittokelvin(String val)  //9) °F to K
    {
        String ans=val;
        double input=Double.parseDouble(val);
        ans=Double.toString((input-32)*5/9+273.15);
        return ans;
    }
    
    
